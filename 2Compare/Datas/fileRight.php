Cũng như con người, máy tính cũng "nói" các ngôn ngữ khác nhau. 
Một số “đọc” và “ghi” từ trái sang phải, trong khi một số khác thì ngược lại. 
Tôi là sinh viên UIT.
Một bộ máy có thể đọc và xử lý dữ liệu được tạo ra bởi nó một cách bình thường. 
Vấn đề xảy ra khi một bộ máy “khác loại” cố gắng đọc dữ liệu đó. 
Thuật ngữ "endian" diễn tả sự khác nhau về cách đọc và ghi dữ liệu giữa các nền tảng máy tính. 
Bài viết sẽ cung cấp cho các bạn một số kiến thức về big và little endian.